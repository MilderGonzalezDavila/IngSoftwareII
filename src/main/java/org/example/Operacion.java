package org.example;

public class Operacion {

    public int Sumar(int a, int b){
        return a + b;
    }

    public int Dividir(int x, int y) {
        return x / y;
    }

    public int Multiplicar(int x, int y) {
        return x * y;
    }

    public int Restar(int x, int y) {
        return x - y;
    }
}
